<?php

/*
===========================
     OPERADOR RESTO (%)
===========================

% → retorna o RESTO da divisão

Ex:
5 % 2 = 1
10 % 3 = 1
4 % 2 = 0

* Muito usado para:
✔ saber se número é par ou ímpar
✔ contadores
✔ repetições

Par → resto 0
Ímpar → resto 1
*/

$a = 1;
$b = 5;

$resultado = $a % $b;

echo "Resultado = $resultado";

?>
